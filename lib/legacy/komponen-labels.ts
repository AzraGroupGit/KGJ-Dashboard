function lookup(map: Record<number, string>, id: unknown): string | null {
  if (id == null || id === "") return null;
  const num = Number(id);
  if (!Number.isFinite(num)) return null;
  return map[num] ?? `#${num}`;
}

export function komponenLabel(id: unknown): string | null {
  return lookup(KOMPONEN_LABELS, id);
}

export function fontLabel(id: unknown): string | null {
  return lookup(FONT_LABELS, id);
}

export function produkKategoriLabel(id: unknown): string | null {
  return lookup(PRODUK_KATEGORI_LABELS, id);
}
